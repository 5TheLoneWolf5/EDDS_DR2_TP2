package org.example;

/*
* Os nomes escolhidos fazem sentido semântico e facilitam na leitura do código. 
* É utilizado verbos no nome da função para descrever o seu comportamento. Os parâmetros são nomeados de forma simples, mas eficaz. A variável "resultadoSoma" descreve a declaração de forma intuitiva.
* */

class A {
    public static int somarEMultiplicarPorDois(int valor1, int valor2) {
        int resultadoSoma = valor1 + valor2;
        return resultadoSoma * 2;
    }
}
